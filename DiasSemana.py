#1-Capturar un numero de 1 al 7 y dependiendo del número decir que día de la semana es:  (donde 1 es lunes, 2 es martes, ....., 7 es domingo)

num = int(input("Teclea un numero [1-7]:\n>> "))

if num == 1:
    print("LUNES")
elif num == 2:
    print("MARTES")
elif num == 3:
    print("MIERCOLES")
elif num == 4:
    print("JUEVES")
elif num == 5:
    print("VIERNES")
elif num == 6:
    print("SÁBADO")
elif num == 7:
    print("DOMINGO")
else:
    print("Número invalido")

